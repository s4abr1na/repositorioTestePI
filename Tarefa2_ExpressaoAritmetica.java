
public class Tarefa2_ExpressaoAritmetica {

	public static void main(String[] args) {
		// atribuição dos valores às respectivas variáveis 
		int a = 2;
		int b = 7;
		float c = 3;
		int d = 1;
		
		//variável que irá calcular a expressão e resultará em decimal
		float resultado = (a + b) * c / (d - 3);
		
		//comando para apresentar o resultado da expressão aritmética
		System.out.println("Resultado da expressão: " + resultado);
	}
 
}
